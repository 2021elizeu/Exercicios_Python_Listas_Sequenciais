#Faça um Programa que peça dois números e imprima a soma.

a = int(input('Digite um numero:')) # o int determina o valor de numero inteiro, input pergunta 
b = int(input('Digite outro numero:'))
soma = a + b

print (f'A soma entre {a} + {b} é : {soma}') #f funçaõ de string imprime na tela {} as variaveis




